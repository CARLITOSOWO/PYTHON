# EN PYTHON TENEMOS LA CONDICIONAL IF QUE ES DIFERENTE SU ESTRUCTURA A LA DE PHP O JAVA 
# LA CUAL ES 
# PALABRA RESERVADA + CONDICION
#        IF  + CONDICION :

# PYTHON ES ESTRICTAMENTE ORDENADO POR LO CUAL PONER UNA LINEA DE CODIGO CON IDENTACION DIFERENTE MARCA QUE ESTA AFUERA DEL IF 
# SI ESTA EN EL MISMO NIVEL ESTA DENTRO DEL IF 

# IDENTACION
# En informática, es una técnica de notación que consiste en agregar espacios en blanco al principio de las líneas de código para mejorar su legibilidad


x = 5
if x > 5:
    print("X ES MAYOR QUE 5")
    print("DENTRO DEL IF ")
elif x == 5: #FUNCIONA COMO UN CAMINO DIFERENTE ACOMPAÑADO DE UNA CONDICIONAL LA CUAL NOSOTROS PODEMOS DEFINIR
    print("X ES IGUAL QUE 5")
else:
    print("X ES MENOR QUE 5")

print("\n")
print ("FUERA DEL IF")

